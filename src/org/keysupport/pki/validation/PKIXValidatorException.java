package org.keysupport.pki.validation;

public class PKIXValidatorException extends Exception {


	/**
	 * 
	 */
	private static final long serialVersionUID = -3319405741656490397L;

	public PKIXValidatorException() {
		super();
	}

	public PKIXValidatorException(String message) {
		super(message);
	}

	public PKIXValidatorException(Throwable cause) {
		super(cause);
	}

	public PKIXValidatorException(String message, Throwable cause) {
		super(message, cause);
	}

}
